function μa_from_va(va, vaa, (; y, a), (; γ, μR, σR, r, amin))
	va = max(va, eps())
	c = va^(-1 / γ)
	k = (μR - r) / σR^2 * (- va / vaa)
	k = clamp(k, 0.0, a - amin)
	μa = y + r * a + (μR - r) * k - c
	σa = k * σR
	
	(; c, k, μa, σa)
end

